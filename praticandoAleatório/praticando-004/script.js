// Switch statement

const Nota = 'D'

switch (Nota) {
    case 'A':
        console.log('Sua nota foi A')
        break
    case 'B':
        console.log('Sua nota foi B')
        break
    case 'C':
        console.log('Sua nota foi C')
        break
    case 'D':
        console.log('Sua nota foi D')
        break
    case 'E':
        console.log('Sua nota foi E')
        break
    default: {
        console.log('[ERRO] Opção invalida!')
    }    
}   




